    <div class="card justify-content-center mt-4 mr-2 ml-2">
      <div class="card-title ml-5 mt-3 mr-5">
      GAH KEEPING EVERYONE SAFE:
      </div>
      <div class="card-body">
      In a bid to reduce the risk of our service users and staff contracting the COVID-19 virus 
we have implemented the following changes which will remain in place until further 
notice (in line with NHS / government guidance):
        <ul>
          <li>GAH continue to keep our service users and staff updated on governmental guidance 
and action concerning COVID-19. Information is sent to staff via email and shared with 
young people on communal notice boards and during residents’ meetings.</li>
          <li>We have reduced the frequency of individuals coming in and out of the units by 
scheduling staff to work double shifts within a 24 hour period wherever possible.</li>
          <li>The residents visitor ban remains in place at all units until further notice (in line with 
government guidelines).</li>
          <li>All communal areas and shared objects are being cleaned more frequently on a daily 
basis by staff on duty.
</li>
          <li>We are supplying hand sanitisers/ hand wash at all units to encourage young people 
and staff to clean their hands regularly especially when entering the house.</li>
          <li> Disposable Face mask are available at all units for use by young people and staff.</li>
          <li>We continue to stock up on emergency food items and supplies incase there is 
another episode of panic buying.</li>
          <li>Non-residential support staff are encouraged to work from home where possible.</li>
          <li>Social distancing guidelines are being encouraged between service users and staff.</li>
          <li>We are taking all the necessary precautionary measures to prevent the spread of 
coronavirus within our organisation. In line with this , all staff will be required to take a 
self swab covid test ( provided in all homes) 24 hours in advance of commencing a shift 
and test results must be sent to (please fill in Karin).</li>
          <li>Staff members who test positive will not be permitted to come back to work until they 
have completed the required isolation period (as specified by government guidance)</li>
          <li>Staff must be transparent and inform management immediately of any positive test 
results. Failure to do so may result in a dismissal.</li>
        </ul>
      </div>
    </div>


    <div class="card justify-content-center mt-4 mr-2 ml-2">
      <div class="card-title ml-5 mt-3 mr-5">
      COVID-19 CASES / OUTBREAK
      </div>
      <div class="card-body">
      The following measures shall come into effect in the event of services users / staff being 
affected by the coronavirus:
        <ul>
          <li>Young people experiencing symptoms of COVID-19 will be instructed to self isolate 
and supported to arrange for a COVID test . Other residents in the household will also 
be Instructed to self isolate.
</li>
          <li>Service Users will be supported to purchase food shopping whilst in isolation and 
provided with emergency food and essential items if necessary.</li>
          <li>Staff will be required to wear the necessary PPE provided whilst on shift and to 
minimise physical contact with service users.</li>
          <li>Staff and Service Users will be required to wear face mask in communal spaces.</li>
          <li>Staff and Services are required to follow vigorous cleaning routines through the 
placement.</li>
          <li>Staff will support service users to inform Colleges/ work places and other individuals / 
groups that they may have been in contact with.
</li>
        </ul>
      </div>  
