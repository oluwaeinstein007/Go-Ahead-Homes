What is COVID-19 / Coronavirus?
As explained by the World Health Organization (WHO), Coronavirus disease (COVID19) is an infectious disease caused by a newly discovered virus.
Most people infected with the COVID-19 virus will experience mild to moderate 
respiratory illness and recover without requiring special treatment. Older people, and 
those with underlying medical problems are more likely to develop serious illness.
The best way to prevent and slow down transmission is to be well informed about the 
COVID-19 virus, the disease, its causes and how it spreads. Protect yourself and others 
from infection by washing your hands or using an alcohol-based rub frequently and not 
touching your face.
The COVID-19 virus spreads primarily through droplets of saliva or discharge from the 
nose when an infected person coughs or sneezes, so it’s important that you also 
practice respiratory etiquette (for example, by coughing into a flexed elbow). <br>
Source: <a target="_blank" href="https://www.who.int/health-topics/coronavirus#tab=tab_1">Check</a>
