      SYMPTOMS OF COVID-19
      </div>
      <div class="card-body">
      The main symptoms of coronavirus are:
      <ul>
        <li>a high temperature – this means you feel hot to touch on your chest or back (you do 
        not need to measure your temperature).</li>
        <li> a new, continuous cough – this means coughing a lot for more than an hour, or 3 or 
        more coughing episodes in 24 hours (if you usually have a cough, it may be worse than 
        usual).</li>
        <li>a loss or change to your sense of smell or taste – this means you've noticed you 
        cannot smell or taste anything, or things smell or taste different to 
        normal.</li>
      </ul>
      Most people with coronavirus have at least 1 of these symptoms <br>
      Source: <a target="_blank" href="https://www.nhs.uk/conditions/coronavirus-covid-19/symptoms/">Check</a>
